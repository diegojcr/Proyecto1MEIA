    private boolean validar(String usuario, String contra){
        NuevoUsuario nuevousuario = new NuevoUsuario();
        MenuUsuario menuUsuario = new MenuUsuario();
        MenuAdmin menuAdmin = new MenuAdmin();
        try{
            String filePath = "C:/MEIA/Proyecto1MEIA/usuarios.txt";
            File file = new File(filePath);
            Scanner scanner = new Scanner(file);
            if(file.exists()){
                if(file.length()== 0){
                    JOptionPane.showMessageDialog(null, "No existen usuarios, se creará el primer usuario");
                    //crearUsuario(file);
                    nuevousuario.setVisible(true);
                    
                } else{
                    String userGuardado = "";
                    while(scanner.hasNextLine()){
                    String line = scanner.nextLine();
                    String[] parts = line.split("\\|");
                    userGuardado = parts[0].trim();
                    String contraGuardada = parts[3].trim();
                    int rolGuardado = Integer.parseInt(parts[4]);
                    if(usuario.equals(userGuardado) && contra.equals(contraGuardada)){
                        String rolU = "";
                        if(rolGuardado==0){
                            rolU = "Usuario";
                            menuUsuario.mostrarDatos(usuario);
                            menuUsuario.setVisible(true);
                        }else{
                            rolU = "Administrador";
                            menuAdmin.mostrarDatos(usuario);
                            menuAdmin.setVisible(true);
                        }
                        //JOptionPane.showMessageDialog(rootPane, "Usuario: "+usuario+"  Rol: "+rolU, "Datos de Ingreso", JOptionPane.PLAIN_MESSAGE, icono);
                        return true;
                    }
                    
                  }
                    if(!usuario.equals(userGuardado)){
                        JOptionPane.showMessageDialog(null, "El usuario no existe, se creará un nuevo usuario");
                        nuevousuario.setVisible(true);
                    }
                    scanner.close();
                }
            }
        }catch(IOException e){
            e.printStackTrace();
        }
        
        
       return false;
    }
    
    
    
    
